public class MyRectangle {

       private int x; //The x coordinate of the upper left corner of the rectangle
    private int y; //The y-coordinate of the upper-left corner of the rectangle
    private int width;//The width of the rectangle, in pixels
    private int height;//The height of the rectangle, in pixels

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}

